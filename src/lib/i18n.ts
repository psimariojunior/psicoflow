const dictionaries: Record<string, Dict> = { pt, en }

export type Locale = keyof typeof dictionaries

const cookieName = "psihumanis-locale"

export function getLocale(): Locale {
  if (typeof window === "undefined") return "pt"
  const match = document.cookie.match(new RegExp(`(?:^|;\\s*)${cookieName}=([^;]*)`))
  if (match) {
    const val = match[1] as Locale
    if (val === "en" || val === "pt") return val
  }
  const browserLang = navigator.language?.slice(0, 2)
  if (browserLang === "en") return "en"
  return "pt"
}

export function setLocale(locale: Locale) {
  document.cookie = `${cookieName}=${locale};path=/;max-age=31536000;SameSite=Lax`
  window.location.reload()
}

export function t(key: string, locale?: Locale): string {
  const l = locale || getLocale()
  return dictionaries[l]?.[key] || dictionaries.pt[key] || key
}

export function useT() {
  const locale = getLocale()
  return (key: string) => t(key, locale)
}

